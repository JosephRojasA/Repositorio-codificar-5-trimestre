package Estructura_Iterativa_o_Repetitiva;
public class Ejercicio4 {
    public static void main(String[] args) {
        System.out.println("Numeros del 100 al 1: ");
        int i=100;
        while(i>=1)
        {
        System.out.println(i);
        i--;
        }
        }
}
